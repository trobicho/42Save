int	ft_atoi(char *str)
{
	int i;
	int r;

	i=0;
	r=0;
	while(i < 10 && str[i] >= '0' && str[i] <= '9')
	{
		r *= 10;
		r += str[i] - '0';
		i++;
	}
	return r;
}
