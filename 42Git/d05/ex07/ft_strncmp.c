int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	char *c_s1;
	char *c_s2;
	int i;

	c_s1 = s1;
	c_s2 = s2;
	i=0;
	while (i < n && *c_s1 == *c_s2 && (*c_s1 || *c_s2))
	{
		if(c_s1 != '\0')
			c_s1++;
		if(c_s2 != '\0')
			c_s2++;
		i++;
	}
	return (*c_s1 - *c_s2);
}
