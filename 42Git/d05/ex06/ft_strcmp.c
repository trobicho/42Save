int		ft_strcmp(char *s1, char *s2)
{
	char *c_s1;
	char *c_s2;

	c_s1 = s1;
	c_s2 = s2;
	while (*c_s1 == *c_s2 && *c_s1 && *c_s2)
	{
		if(c_s1 != '\0')
			c_s1++;
		if(c_s2 != '\0')
			c_s2++;
	}
	return (*c_s1 - *c_s2);
}
