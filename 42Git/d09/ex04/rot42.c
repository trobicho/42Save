char	*ft_rot42(char *str)
{
	int		rot;
	char	*c;

	c = str;
	rot = 42;
	while (*c)
	{
		if (*c >= 'A' && *c <= 'Z')
			*c = (*c - 'A' + rot) % ('Z' - 'A' + 1) + 'A';
		else if (*c >= 'a' && *c <= 'z')
			*c = (*c - 'a' + rot) % ('z' - 'a' + 1) + 'a';
		c++;
	}
	return (str);
}
