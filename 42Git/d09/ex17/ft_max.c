int	ft_max(int *tab, int length)
{
	int max;
	int i;

	if (!tab)
		return (0);
	max = tab[0];
	i = 0;
	while (i < length)
	{
		if (max < tab[i])
			max = tab[i];
	}
	return (max);
}
