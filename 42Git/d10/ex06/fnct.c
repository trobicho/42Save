int	myft_add(int nb1, int nb2)
{
	return (nb1 + nb2);
}

int	myft_sub(int nb1, int nb2)
{
	return (nb1 - nb2);
}

int	myft_mul(int nb1, int nb2)
{
	return (nb1 * nb2);
}

int	myft_div(int nb1, int nb2)
{
	return (nb1 / nb2);
}

int	myft_mod(int nb1, int nb2)
{
	return (nb1 % nb2);
}
