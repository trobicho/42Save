char	*ft_strlowcase(char *str)
{
	char *c;

	c = str;
	while (*c)
	{
		if(*c >= 'A' && *c <= 'Z')
		{
			*c = *c - 'A' + 'a';
		}
		c++;
	}
	return str;
}
